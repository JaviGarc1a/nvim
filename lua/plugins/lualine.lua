local function lazy_stats()
	local ok, lazy = pcall(require, "lazy")
	if not ok then
		return ""
	end
	local stats = lazy.stats()
	return string.format(" %d/%d plugins in %.2fms", stats.loaded, stats.count, stats.startuptime)
end

